const errors = {
  'zip-code': 'Type your 5 digit Zip Code',
  'pass-confirmation': 'Passwords do not match',
  email: "Incorrect email. Format must be 'john@doe.com",
  password: 'Minimum eight characters, at least one uppercase letter, one lowercase letter and one number',
  country: 'Choose a country from the list',
};


function showError(input) {
  const errorContainer = input.nextElementSibling;

  if (input.validity.patternMismatch) {
    errorContainer.textContent = errors[input.name]
  }

};


const form = document.querySelector('.form');
form.addEventListener('submit', function(e) {
  console.log(e);
})


const email = document.querySelector('#email');
email.addEventListener('input', function(e) {
  if (email.reportValidity()) {
    e.target.nextElementSibling.textContent = ''
  } else {
    e.target.nextElementSibling.textContent = errors['email']
  }
})


const countryDataList = document.querySelector('#list-of-countries')

countryList.forEach(countryName => {
  const newOption = document.createElement('option')
  newOption.value = countryName;
  countryDataList.appendChild(newOption)
})


const country = document.querySelector('#country');
country.addEventListener('input', function(e) {
  const countryChosen = e.target.value;

  if (countryList.includes(countryChosen)) {
    e.target.nextElementSibling.textContent = ''
    e.target.setCustomValidity('')
  } else {
    e.target.setCustomValidity(errors['country'])
    e.target.nextElementSibling.textContent = errors['country']
  }
})




const zipCode = document.querySelector('#zip-code');
zipCode.addEventListener('input', function(e) {
  e.target.validity.valid ? e.target.nextElementSibling.textContent = ''
                          : showError(e.target);  
})



const password = document.querySelector('#password')
password.addEventListener('input', function(e) {
  e.target.validity.valid ? e.target.nextElementSibling.textContent = ''
  : showError(e.target);
}) 



const passConfirmation = document.querySelector('#pass-confirmation')
passConfirmation.addEventListener('input', function(e) {
  const currentPassword = password.value
  console.log(currentPassword);
  if (currentPassword == e.target.value) {
    e.target.nextElementSibling.textContent = ''
    e.target.setCustomValidity('')

  } else {
    e.target.setCustomValidity(errors['pass-confirmation'])
    e.target.nextElementSibling.textContent = errors['pass-confirmation']
  }
})












